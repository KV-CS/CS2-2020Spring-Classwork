# Function Print number of chars in an integer

def howManyChars(someInt):
   someStr = str(someInt)
   return len(someStr)

print(howManyChars(54346))
print(howManyChars(23))
print(howManyChars(23471274596126453624560234))
print(howManyChars('a'))


